fn main(){
    let sum = 2+4;
    println!("Sum : {}",sum);

    let subtract = 4-5;
    println!("Difference : {}",subtract);

    let multiplication = 7*7;
    println!("Multiplication : {}",multiplication);

    let division: f64 = 8.0/3.0;
    println!("Division : {}",division);

    let t = true;
    println!("The value is {}",t);

    let f: bool = false;
    println!("The value is {}",f);

    let c = "z";
    println!("{}",c);

    let z: char = 'a';
    println!("{}",z);

// when declared char then compulsory to use single quote.
}
